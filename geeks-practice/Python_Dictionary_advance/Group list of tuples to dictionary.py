#Input: [(1, 2, 3, 4, 5, 6), (1, 4),  (3, 5, 3, 4, 5, 6), (5, 7),    (5, 6), (4, 4)]
#Output:{1: [2, 3, 4, 5, 6, 4], 3: (5, 3, 4, 5, 6), 5: [7, 6], 4: [4]}

ls = [(1, 2, 3, 4, 5, 6), (1, 4),  (3, 5, 3, 4, 5, 6), (5, 7),    (5, 6), (4, 4)]

dic = {}
for l in ls:
    if l[0] in dic:
        dic[l[0]] = dic.get(l[0]) + l[1:]
    else:
        dic[l[0]] = l[1:]
        
print(dic)